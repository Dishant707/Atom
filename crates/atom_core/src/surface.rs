pub fn generate_surface(system: &System, probe_radius: f32, grid_res: f32) -> SurfaceMesh {
    let padding = probe_radius + 4.0;
    
    // Bounds of the system
    let mut min = Vec3::splat(f32::MAX);
    let mut max = Vec3::splat(f32::MIN);
    for &pos in &system.positions {
        min = min.min(pos);
        max = max.max(pos);
    }
    
    min -= Vec3::splat(padding);
    max += Vec3::splat(padding);
    let size = max - min;
    
    let dim_x = (size.x / grid_res).ceil() as usize;
    let dim_y = (size.y / grid_res).ceil() as usize;
    let dim_z = (size.z / grid_res).ceil() as usize;
    
    let _dimensions = (dim_x, dim_y, dim_z);
    let num_cells = dim_x * dim_y * dim_z;
    
    // Optimisation: Use a SpatialGrid to speed up distance queries
    // We can reuse the one from system or create a temp one.
    // Cell size for optimization grid should be max atom radius + probe radius + epsilon
    // Max VDW is usually ~2.0A. Probe ~1.4A. So 3.5-4.0A cell size.
    let search_radius = 2.0 + probe_radius;
    let _spatial_grid = SpatialGrid::new(system.box_size, search_radius);
    // Actually our own bounds might differ from system box, but SpatialGrid is periodic/infinite based.
    // Let's just do a brute force optimization: Grid cells map to bins.
    
    // Helper to Get VDW radius
    let get_radius = |z: u8| -> f32 {
        match z {
            1 => 1.2, 6 => 1.7, 7 => 1.55, 8 => 1.52, 15 => 1.8, 16 => 1.8, _ => 1.5
        }
    };

    println!("Generating Surface Grid: {}x{}x{} = {} voxels", dim_x, dim_y, dim_z, num_cells);

    // Compute Scalar Field (Distance to Surface)
    // Positive = Inside, Negative = Outside (or vice versa, let's stick to standard SDF)
    // Field = (Distance to nearest atom CENTER) - (Atom Radius + Probe Radius)
    // If Field < 0, we are "Inside" the expanded shape.
    let mut field = vec![0.0f32; num_cells];
    
    // For large grids, parallelize (requires Rayon)
    // Since we don't have Rayon imported in this file yet (it is in lib.rs though), 
    // let's assume we can use it or just serial for now. 
    // It is in dependencies, but we need to verify import.
    // We'll stick to serial for simplicity unless it's too slow.
    
    let origin = min;
    
    // Optimization: Loop atoms and splat them onto the grid?
    // "Splatting" is O(Atoms * Radius^3), reading is O(Grid * Atoms).
    // Splatting is faster for sparse atoms.
    
    // Initialize field to huge positive distance (Empty space)
    field.fill(1000.0);
    
    for (i, &atom_pos) in system.positions.iter().enumerate() {
        let r = get_radius(system.atomic_numbers[i]) + probe_radius;
        let r_search = r + grid_res * 2.0; // Influences slightly beyond r due to grid sampling
        
        // Find grid bounds for this atom
        let min_idx = ((atom_pos - origin - Vec3::splat(r_search)) / grid_res).max(Vec3::ZERO);
        let max_idx = ((atom_pos - origin + Vec3::splat(r_search)) / grid_res).min(size / grid_res);
        
        let i_min = min_idx.x as usize;
        let j_min = min_idx.y as usize;
        let k_min = min_idx.z as usize;
        let i_max = max_idx.x as usize;
        let j_max = max_idx.y as usize;
        let k_max = max_idx.z as usize;
        
        let _r_sq = r * r;
        
        for k in k_min..=k_max {
             if k >= dim_z { continue; }
             for j in j_min..=j_max {
                 if j >= dim_y { continue; }
                 for i in i_min..=i_max {
                     if i >= dim_x { continue; }
                     
                     let idx = i + j * dim_x + k * dim_x * dim_y;
                     let pt = origin + Vec3::new(i as f32, j as f32, k as f32) * grid_res;
                     let d = pt.distance(atom_pos);
                     let val = d - r; // Negative inside, Positive outside
                     
                     if val < field[idx] {
                         field[idx] = val;
                     }
                 }
             }
        }
    }
    
    // Marching Cubes
    let mut vertices = Vec::new();
    let mut normals = Vec::new();
    let mut indices = Vec::new();
    let mut values = Vec::new();
    
    let iso_level = 0.0;
    
    for k in 0..dim_z - 1 {
        for j in 0..dim_y - 1 {
            for i in 0..dim_x - 1 {
                let idx = i + j * dim_x + k * dim_x * dim_y;
                
                // 8 corners
                // 0: x,y,z
                // 1: x+1,y,z
                // 2: x+1,y,z+1
                // 3: x,y,z+1
                // 4: x,y+1,z
                // 5: x+1,y+1,z
                // 6: x+1,y+1,z+1
                // 7: x,y+1,z+1
                
                let _p = [
                    idx,
                    idx + 1,
                    idx + 1 + dim_x * dim_y, // Note: Standard MC usually Y is up, let's be careful with strides
                    idx + dim_x * dim_y
                ];
                // Wait, standard MC order:
                // 0: 0,0,0
                // 1: 1,0,0
                // 2: 1,0,1
                // 3: 0,0,1  (Z is forward/up?)
                
                // Let's use standard stride:
                // stride x = 1, stride y = dim_x, stride z = dim_x*dim_y
                let sx = 1;
                let sy = dim_x;
                let sz = dim_x * dim_y;
                
                let _corners = [
                    idx,            // 0: 000
                    idx + sx,       // 1: 100
                    idx + sx + sy,  // 2: 110 (Note this differs from some tables!)
                    idx + sy,       // 3: 010
                    idx + sz,       // 4: 001
                    idx + sx + sz,  // 5: 101
                    idx + sx + sy + sz, // 6: 111
                    idx + sy + sz,  // 7: 011
                ];
                // Check table ordering
                // V0: 000, V1: 100, V2: 110, V3: 010 (Bottom Face CCW)
                // V4: 001, V5: 101, V6: 111, V7: 011 (Top Face CCW)
                // Paul Bourke's:
                // 0: x,y,z
                // 1: x+1,y,z
                // 2: x+1,y,z+1  <-- WAIT, 2 is (1,0,1) in Bourke?
                // Bourke:
                // 0: (0,0,0), 1: (1,0,0), 2: (1,0,1), 3: (0,0,1)
                // 4: (0,1,0), 5: (1,1,0), 6: (1,1,1), 7: (0,1,1)
                
                // Let's switch to Bourke's indexing to match table
                // Corners relative to (i, j, k)
                // 0: (0,0,0) -> idx
                // 1: (1,0,0) -> idx + sx
                // 2: (1,0,1) -> idx + sx + sz
                // 3: (0,0,1) -> idx + sz
                // 4: (0,1,0) -> idx + sy
                // 5: (1,1,0) -> idx + sx + sy
                // 6: (1,1,1) -> idx + sx + sy + sz
                // 7: (0,1,1) -> idx + sy + sz
                
                let c_idx = [
                    idx,
                    idx + sx,
                    idx + sx + sz,
                    idx + sz,
                    idx + sy,
                    idx + sx + sy,
                    idx + sx + sy + sz,
                    idx + sy + sz
                ];
                
                let mut cube_index = 0;
                if field[c_idx[0]] < iso_level { cube_index |= 1; }
                if field[c_idx[1]] < iso_level { cube_index |= 2; }
                if field[c_idx[2]] < iso_level { cube_index |= 4; }
                if field[c_idx[3]] < iso_level { cube_index |= 8; }
                if field[c_idx[4]] < iso_level { cube_index |= 16; }
                if field[c_idx[5]] < iso_level { cube_index |= 32; }
                if field[c_idx[6]] < iso_level { cube_index |= 64; }
                if field[c_idx[7]] < iso_level { cube_index |= 128; }
                
                let edges = EDGE_TABLE[cube_index];
                if edges == 0 { continue; }
                
                // Vertices of the cube
                let pos = origin + Vec3::new(i as f32, j as f32, k as f32) * grid_res;
                let v = [
                    pos,                                  // 0
                    pos + Vec3::new(grid_res, 0.0, 0.0), // 1
                    pos + Vec3::new(grid_res, 0.0, grid_res), // 2
                    pos + Vec3::new(0.0, 0.0, grid_res), // 3
                    pos + Vec3::new(0.0, grid_res, 0.0), // 4
                    pos + Vec3::new(grid_res, grid_res, 0.0), // 5
                    pos + Vec3::new(grid_res, grid_res, grid_res), // 6
                    pos + Vec3::new(0.0, grid_res, grid_res), // 7
                ];
                
                let val = [
                    field[c_idx[0]], field[c_idx[1]], field[c_idx[2]], field[c_idx[3]],
                    field[c_idx[4]], field[c_idx[5]], field[c_idx[6]], field[c_idx[7]],
                ];
                
                let mut vert_list = [Vec3::ZERO; 12];
                
                // Interpolate
                let interp = |i1, i2| -> Vec3 {
                     let p1 = v[i1];
                     let p2 = v[i2];
                     let val1 = val[i1];
                     let val2 = val[i2];
                     
                     let diff = f32::abs(val2 - val1);
                     if diff < 1e-5_f32 {
                         p1
                     } else {
                         let mu = (iso_level - val1) / (val2 - val1);
                         p1 + (p2 - p1) * mu
                     }
                };

                if (edges & 1) != 0 { vert_list[0] = interp(0, 1); }
                if (edges & 2) != 0 { vert_list[1] = interp(1, 2); }
                if (edges & 4) != 0 { vert_list[2] = interp(2, 3); }
                if (edges & 8) != 0 { vert_list[3] = interp(3, 0); }
                if (edges & 16) != 0 { vert_list[4] = interp(4, 5); }
                if (edges & 32) != 0 { vert_list[5] = interp(5, 6); }
                if (edges & 64) != 0 { vert_list[6] = interp(6, 7); }
                if (edges & 128) != 0 { vert_list[7] = interp(7, 4); }
                if (edges & 256) != 0 { vert_list[8] = interp(0, 4); }
                if (edges & 512) != 0 { vert_list[9] = interp(1, 5); }
                if (edges & 1024) != 0 { vert_list[10] = interp(2, 6); }
                if (edges & 2048) != 0 { vert_list[11] = interp(3, 7); }
                
                let mut table_idx = (cube_index as usize) * 16;
                while TRI_TABLE[table_idx] != -1 {
                    let v0_idx = TRI_TABLE[table_idx] as usize;
                    let v1_idx = TRI_TABLE[table_idx+1] as usize;
                    let v2_idx = TRI_TABLE[table_idx+2] as usize;
                    
                    let p0 = vert_list[v0_idx];
                    let p1 = vert_list[v1_idx];
                    let p2 = vert_list[v2_idx];
                    
                    // Add vertices
                    let start_idx = vertices.len() as u32;
                    vertices.push(p0);
                    vertices.push(p1);
                    vertices.push(p2);
                    indices.push(start_idx);
                    indices.push(start_idx + 1);
                    indices.push(start_idx + 2);
                    
                    // Simple Face Normal
                    let n = (p1 - p0).cross(p2 - p0).normalize();
                    normals.push(n);
                    normals.push(n);
                    normals.push(n);
                    
                    // Default values (0.0 for now)
                    // TODO: Map properties
                    values.push(0.0);
                    values.push(0.0);
                    values.push(0.0);
                    
                    table_idx += 3;
                }
            }
        }
    }
    
    println!("Generated Mesh: {} vertices, {} indices", vertices.len(), indices.len());
    
    SurfaceMesh {
        vertices,
        normals,
        indices,
        values,
    }
}
